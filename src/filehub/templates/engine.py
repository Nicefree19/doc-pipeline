    def load_organize_templates(self, templates: list[OrganizeTemplate]) -> None:
        """Register additional organize templates (from user config).

        User-supplied templates override built-in templates with the same name.
        """
        for tmpl in templates:
            self.organize_templates[tmpl.name] = tmpl

    def get_organize_template(self, name: str) -> OrganizeTemplate | None:
        """Get organize template by name."""
        return self.organize_templates.get(name)

    def list_organize_templates(self) -> list[str]:
        """List available organize template names."""
        return list(self.organize_templates.keys())
